message = "  Welcome to PYTHON Program Language  ";
# len() Will total No of Characters
print(len(message))

# this will return total string
print(message[:])

# THis will return from ) Chanracter to 10th Charceter
print(message[0:10])

# THis will remove the extra spaces
print(message.strip())

# This will convert all the characters in to Lowercase
print(message.lower())

# This will return uppercase Letters
print(message.upper())

# Replace will replace the given charcters
print(message.replace("Welcome","Hello "))

print(message.split(" "))